def query_huggingface_model(prompt: str, max_tokens=200):
    if LLM_CLIENT is None:
        print("DEBUG: LLM_CLIENT is not initialized. Cannot make API call.")
        return "Error: Hugging Face InferenceClient not configured. Please check HF_TOKEN."

    try:
        completion = LLM_CLIENT.chat.completions.create(
            model=LLM_MODEL_NAME, 
            messages=[
                {"role": "user", "content": prompt}
            ],
            max_tokens=max_tokens,
        )
        
        generated_text = completion.choices[0].message.content
        
        if not isinstance(generated_text, str) or not generated_text.strip():
            print(f"DEBUG: LLM returned non-string or empty content: {generated_text}")
            return "No meaningful response generated by the LLM."
        
        print(f"DEBUG: LLM Generated Text (first 50 chars): {generated_text[:50]}...")
        return generated_text.strip()
        
    except Exception as e:
        print(f"DEBUG: LLM InferenceClient error: {e}")
        traceback.print_exc()
        return f"Error: LLM call failed with {type(e).__name__} - {e}. Check terminal for details."

