def intake_agent(state: dict) -> Command:
    logs: dict = state.get("user_logs", {})
    date_str: str = state.get("date")
    if not logs or not date_str:
        raise ValueError("state must include 'user_logs' and 'date'")
    
    sleep = float(logs.get("sleep", 0.0))
    exercise = float(logs.get("exercise", 0.0))
    productivity = float(logs.get("productivity", 0.0))
    self_help = float(logs.get("self_help", 0.0))

    cursor.execute("""
    INSERT INTO daily_metrics (
        date, sleep_hours, exercise_hours, productivity_hours, self_help_hours
    ) VALUES (?, ?, ?, ?, ?)
    ON CONFLICT(date) DO UPDATE SET
        sleep_hours        = excluded.sleep_hours,
        exercise_hours     = excluded.exercise_hours,
        productivity_hours = excluded.productivity_hours,
        self_help_hours    = excluded.self_help_hours
    """, (date_str, sleep, exercise, productivity, self_help))
    conn.commit()

    text = "; ".join(f"{k}: {v}" for k,v in logs.items())

    embedding = embed_model.encode(text).tolist()
    logs_collection.add(
      documents=[text],
      embeddings=[embedding],
      metadatas=[{"type":"daily_log","timestamp":date_str}],
      ids=[date_str]
    )

    command_to_return = Command(update={"user_input": logs.get("notes","")}, goto="GoalAgent")
    print(f"DEBUG: Intake Agent returning: {command_to_return}")
    return command_to_return

def goal_agent(state: dict) -> Command:
    today_dt = datetime.fromisoformat(state["date"])
    week_ago_dt = today_dt - timedelta(days=6)
    
    dates = [(week_ago_dt + timedelta(days=i)).date().isoformat() for i in range(7)]

    stored = logs_collection.get(ids=dates)
    contexts = stored.get("documents", [])

    initial_notes = state.get("user_logs", {}).get("notes", "").strip()
    if contexts:
        context_text = "Previous logs:\n" + "\n".join(f"- {doc}" for doc in contexts if doc)
    elif initial_notes:
        context_text = f"No previous logs. User notes: {initial_notes}"
    else:
        context_text = "No previous logs or notes available. Proceed to set an initial goal."

    cursor.execute(
        """
        SELECT 
          AVG(sleep_hours),
          AVG(exercise_hours),
          AVG(productivity_hours),
          AVG(self_help_hours)
        FROM daily_metrics
        WHERE date BETWEEN ? AND ?
        """, (dates[0], dates[-1]) 
    )
    row = cursor.fetchone()
    
    avg_sleep, avg_exercise, avg_prod, avg_self = 0.0, 0.0, 0.0, 0.0
    if row and any(val is not None for val in row):
        avg_sleep, avg_exercise, avg_prod, avg_self = [val or 0.0 for val in row]

    prompt = f"""
    You are a wellness coach. Here is the context:
    {context_text}

    Their average metrics over the past week (if any):
    • Sleep:        {avg_sleep  :.1f} hrs/day
    • Exercise:     {avg_exercise:.1f} hrs/day
    • Productivity: {avg_prod   :.1f} hrs/day
    • Self‑help:    {avg_self   :.1f} hrs/day

    Based on this (or the initial notes), propose one weekly SMART goal to improve their wellness balance. 
    Return exactly in this format:

    Goal: <one-sentence SMART goal>

    S: <Specific aspect>
    M: <How to measure>
    A: <Why it’s achievable>
    R: <Why it’s relevant>
    T: <Time-bound target>
    """
    output = query_huggingface_model(prompt)

    command_to_return = Command(
        goto="Planner",
        update={"weekly_goal": output}
    )
    print(f"DEBUG: Goal Agent returning: {command_to_return}")
    return command_to_return

def planner_agent(state: dict) -> Command:
    weekly_goal = state.get("weekly_goal")
    print(f"DEBUG: Planner Agent - received weekly_goal: {weekly_goal}")
    start_date  = datetime.fromisoformat(state.get("date"))

    if not weekly_goal or "Error:" in weekly_goal or "No meaningful response" in weekly_goal:
        command_to_return = Command(
            goto="Advisor",
            update={"daily_tasks": {}}
        )
        print(f"DEBUG: Planner Agent (no goal) returning: {command_to_return}")
        return command_to_return

    prompt = f"""
    You are a task planning assistant. Given the following SMART weekly goal:

    {weekly_goal}

    Split this goal into one clear, actionable task for each day of the next 7 days.
    Return as a numbered list, with each line in the format:<Date>: <Task>
    Use ISO date formatYYYY-MM-DD, starting from {start_date.date().isoformat()}.
    Ensure each line starts with a date inYYYY-MM-DD format followed by ':'.
    Example:
    2025-06-18: Meditate for 10 minutes.
    2025-06-19: Go for a 30-minute walk.
    """
    result = query_huggingface_model(prompt)
    print(f"DEBUG: Planner Agent - raw LLM output for tasks: {result}")

    daily_tasks = {}
    if "Error:" in result or "No meaningful response" in result:
        command_to_return = Command(
            goto="Advisor",
            update={"daily_tasks": {}}
        )
        print(f"DEBUG: Planner Agent (LLM error) returning: {command_to_return}")
        return command_to_return

    for line in result.splitlines():
        line = line.strip()
        if line and ':' in line:
            parts = line.split(':', 1)
            date_str = parts[0].strip()
            task = parts[1].strip() if len(parts) > 1 else ""

            try:
                datetime.fromisoformat(date_str) 
                if task:
                    daily_tasks[date_str] = task
            except ValueError:
                continue
            
    if not daily_tasks:
        command_to_return = Command(
            goto="Advisor",
            update={"daily_tasks": {}} # No tasks, no advice from Planner
        )
        print(f"DEBUG: Planner Agent (no tasks parsed) returning: {command_to_return}")
        return command_to_return

    for date_str, task in daily_tasks.items():
        cursor.execute(
            "INSERT OR IGNORE INTO daily_tasks(date, task, done) VALUES (?, ?, 0)",
            (date_str, task)
        )
    conn.commit()

    command_to_return = Command(
        goto="Advisor",
        update={"daily_tasks": daily_tasks} 
    )
    print(f"DEBUG: Planner Agent returning: {command_to_return}")
    return command_to_return

def advisor_agent(state: dict) -> Command:
    current_advice = str(state.get("weekly_advice", "")).strip() 
    print(f"DEBUG: Advisor Agent - received state (weekly_advice check): '{state.get('weekly_advice')}' (type: {type(state.get('weekly_advice'))})")
    print(f"DEBUG: Advisor Agent - current_advice after .get and str(): '{current_advice}' (type: {type(current_advice)})")

    end_date = datetime.fromisoformat(state["date"]).date()
    start_date = end_date - timedelta(days=6)
    dates = [(start_date + timedelta(days=i)).isoformat() for i in range(7)]

    task_entries = []
    cursor.execute(
        f"SELECT date, task, done FROM daily_tasks WHERE date BETWEEN ? AND ? ORDER BY date",
        (dates[0], dates[-1])
    )
    rows = cursor.fetchall()

    final_advice = ""

    if rows: 
        for d, task, done in rows:
            status = "done" if done else "not done"
            task_entries.append(f"{d}: {task} - {status}")
        tasks_summary = "\n".join(task_entries)

        stored = logs_collection.get(ids=dates)
        log_docs = stored.get("documents", [])
        logs_text = "\n".join(f"- {doc}" for doc in log_docs if doc)
        context_text = logs_text or "No detailed logs available for the past week."

        prompt = f"""
        You are a supportive wellness coach. Here are the details for the past week:

        Daily Logs:
        {context_text}

        Task Completion:
        {tasks_summary}

        Please provide:
        1. Praise for what the user did well.
        2. Constructive feedback on missed tasks.
        3. Suggestions to improve adherence next week.
        4. Any motivational encouragement.

        Return a concise paragraph of advice.
        """
        
        generated_advice = query_huggingface_model(prompt)
        print(f"DEBUG: Advisor Agent - raw LLM output for advice: '{generated_advice}' (type: {type(generated_advice)})")
        
        if "Error:" in generated_advice or "No meaningful response" in generated_advice:
            final_advice = f"Advisor could not generate detailed advice: {generated_advice}"
        else:
            final_advice = generated_advice
            
    else: 
        weekly_goal = state.get("weekly_goal", "") # Getting the goal to provide better context
        
        if "Error:" in weekly_goal or "No meaningful response" in weekly_goal:
            final_advice = "No specific weekly goal was set, so no detailed tasks could be planned. Please ensure your input helps generate a clear goal."
        elif not weekly_goal.strip():
            final_advice = "No weekly goal provided, so no tasks could be planned for advice."
        else:
            final_advice = f"A goal was set ({weekly_goal}), but no tasks were generated for the week. Please refine your goal or task planning."

    final_advice = str(final_advice).strip()
    print(f"DEBUG: Advisor Agent - final_advice to return: '{final_advice}' (type: {type(final_advice)})")

    command_to_return = Command(
        goto=END,
        update={"weekly_advice": final_advice}
    )
    print(f"DEBUG: Advisor Agent returning: {command_to_return}")
    return command_to_return

